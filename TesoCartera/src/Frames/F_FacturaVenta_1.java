    void SumaTabla() {

        SubTotal = 0;
        double ValorTabla = 0;
        try {
            int Filas = jTable2.getRowCount();

            if (Filas > 0) {
                for (int i = 0; i < Filas; i++) {
                    ValorTabla = Double.valueOf(jTable2.getValueAt(i, 5).toString());

                    SubTotal = SubTotal + ValorTabla;
                    System.out.println("Sumando " + SubTotal + " + " + ValorTabla);
//        System.out.println("sale " + suma);
                    jTextField5.setText(Df.format(SubTotal));

                    CalcularCostoNeto(CalcularIva(), CalcularDescuento());
                }
            } else {
                jTextField5.setText("0");
                jTextField2.setText("0");
                jTextField3.setText("0");
                jLabel8.setText("0");
            }

        } catch (Exception e) {
            System.out.println("Error Metodo SumaTabla");
        }

    }

