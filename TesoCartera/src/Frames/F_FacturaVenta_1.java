    public double CalcularIva() {

        double TotalIva = 0;
        int Iva = 0;

        Iva = Integer.valueOf(jTextField2.getText().toString());
        if (Iva == 0) {
            return 0;
        } else {

            TotalIva = (SubTotal * Iva) / 100;

            return TotalIva;

        }

    }

    public double CalcularDescuento() {

        double TotalDescuento = 0;
        int Descuento = 0;

        Descuento = Integer.valueOf(jTextField3.getText().toString());
        if (Descuento == 0) {
            return 0;
        } else {

            TotalDescuento = (SubTotal * Descuento) / 100;

            return TotalDescuento;

        }

    }

