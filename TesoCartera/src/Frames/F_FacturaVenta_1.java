    void ValidarProductoTablaDetalleF() {

        int BanderaProducto = 0;
        int FilaListaProductos = JTB_JDProducto.getSelectedRow();
        int Fila = jTable2.getRowCount();
        try {
            String Producto = JTB_JDProducto.getValueAt(FilaListaProductos, 0).toString();

            if (Fila == 0) {
                ObtenerDatosListaProductos();
                System.out.println("la tabla esta vacia");
            } else {
                for (int j = 0; j < Fila; j++) {
                    String Aux = jTable2.getValueAt(j, 0).toString();

                    if (!Producto.equals(Aux)) {
                        System.out.println("los valores: " + Producto + " y " + Aux + "  son distintos");
                        BanderaProducto = BanderaProducto + 0;
//                        ObtenerDatosListaProductos();
                        //-----------------------------------------------------------------
                    }
                    if (Producto.equals(Aux)) {

                        System.out.println("los valores: " + Producto + " y " + Aux + "  son iguales");
                        BanderaProducto = BanderaProducto + 1;
//                    JOptionPane.showMessageDialog(null, "Solo Puede Haber Un Producto De Cada Uno","Error Producto",JOptionPane.ERROR_MESSAGE);
                        break;

                    }
                }
                if (BanderaProducto == 0) {
                    ObtenerDatosListaProductos();
                }
                if (BanderaProducto == 1) {
                    JOptionPane.showMessageDialog(null, "Solo Puede Haber Un Producto De Cada Uno", "Error Producto", JOptionPane.ERROR_MESSAGE);
                    BanderaProducto = BanderaProducto - 1;
                }

            }
        } catch (Exception e) {
            System.out.println("error en metod validarproducto tabla detalles");
        }

    }

