    void CostoPromedioProducto() {

        int Filas = jTable2.getRowCount();

        if (Filas > 0) {
            for (int i = 0; i < Filas; i++) {

                String Producto = jTable2.getValueAt(i, 0).toString();
                double Cantidad = Double.valueOf(jTable2.getValueAt(i, 4).toString());

                //precio_producto=(precio_producto+10000)/2 where id_producto='AGPT0119'
                SentenciaSql = "precio_producto=(precio_producto+" + Cantidad + ")/2 where id_producto='" + Producto + "'";
                
                m.ModificarHidden("producto", SentenciaSql);
            }
        }else{
            
            System.out.println("No Tiene Ningun Producto Para Modificar");
        }

    }
    void ActualizarStock() {

        int Filas = jTable2.getRowCount();

        if (Filas > 0) {
            for (int i = 0; i < Filas; i++) {

                String Producto = jTable2.getValueAt(i, 0).toString();
                double Cantidad = Double.valueOf(jTable2.getValueAt(i, 3).toString());

                SentenciaSql = "stock_actual=stock_actual+" + Cantidad + " where id_producto='" + Producto + "'";
                
                m.ModificarHidden("producto", SentenciaSql);
            }
        }else{
            
            System.out.println("No Tiene Ningun Producto Para Modificar");
        }

    }

