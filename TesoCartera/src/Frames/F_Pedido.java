    void ListarDetallePedidoInicial() {
        String Sql = "select detalle_pedido.id_producto,"
                + "producto.descripcion_producto,producto.unidad_medida,detalle_pedido.cantidad_producto,"
                + "producto.precio_producto,detalle_pedido.total from detalle_pedido,producto,pedido "
                + "where detalle_pedido.id_pedido=pedido.id_pedido && "
                + "detalle_pedido.id_producto=producto.id_producto && detalle_pedido.id_pedido=1000000000000";
        m.Listar(Sql, jTable2);
    }
    void ListarPedido() {
        String FiltroSql = jTextField4.getText();
        String Sql = "select pedido.id_pedido, cliente.nombre_cliente, pedido.total_pedido, "
                + "pedido.fecha_pedido from pedido,cliente where pedido.id_cliente=cliente.id_cliente "
                + "&& concat(pedido.id_pedido, cliente.nombre_cliente) like '%" + FiltroSql + "%' order by pedido.id_pedido asc";
        m.Listar(Sql, JTB_JDFacturaV);
        ListarPedido_Aux();
    }
    void ListarPedido_Aux() {
        String FiltroSql = jTextField4.getText();
        String Sql = "select pedido.id_pedido, pedido.num_pedido,pedido.cuota_iva_pedido,"
                + "pedido.descuento_pedido,pedido.id_cliente,cliente.nombre_cliente, "
                + "pedido.fecha_pedido,pedido.observaciones from pedido,cliente where pedido.id_cliente=cliente.id_cliente "
                + "&& concat(pedido.id_pedido,cliente.nombre_cliente) like '%" + FiltroSql + "%' order by pedido.id_pedido asc";
        m.Listar(Sql, JTB_Auxiliar);
    }

