    void Consultar() {

        SentenciaSql = "select facturas.id_factura, facturas.num_factura, facturas.forma_pago, facturas.cuota_iva_factura,"
                + "facturas.descuento_factura,cliente.nombre_cliente,facturas.total_factura, "
                + "facturas.fecha_factura,facturas.observaciones from facturas,cliente where facturas.id_cliente=cliente.id_cliente ";

        //SentenciaSql = "select * from producto";
        String Aux;
        String Filtro = jComboBox1.getSelectedItem().toString();
        String Tipo = jComboBox3.getSelectedItem().toString();

        if (Filtro == "No") {

            SentenciaSql = SentenciaSql + " order by facturas.id_factura asc";
            jComboBox3.setVisible(false);
            jComboBox2.setVisible(false);
            jTextField1.setVisible(false);
            jLabel5.setVisible(false);
            jLabel2.setVisible(false);
            jDateChooser1.setVisible(false);
            jDateChooser2.setVisible(false);
            m.Listar(SentenciaSql, jTable1);
            ConteoTabla();

        } else {

            if (Tipo == "Id De La Factura") {

                Aux = SentenciaSql + " && facturas.id_factura=" + jTextField1.getText() + " order by facturas.id_factura asc";
                m.Listar(Aux, jTable1);

            }
            if (Tipo == "Numero De La Factura") {

                Aux = SentenciaSql + " && facturas.num_factura like '%" + jTextField1.getText() + "%' order by facturas.id_factura asc";
                m.Listar(Aux, jTable1);
            }
            if (Tipo == "Monto Iva") {

                Aux = SentenciaSql + " && facturas.cuota_iva_factura='" + jTextField1.getText() + "' order by facturas.id_factura asc";
                m.Listar(Aux, jTable1);
            }
            if (Tipo == "Monto Descuento") {

                Aux = SentenciaSql + " && facturas.descuento_factura='" + jTextField1.getText() + "' order by facturas.id_factura asc";
                m.Listar(Aux, jTable1);
            }
            if (Tipo == "Cliente") {

                Aux = SentenciaSql + " && facturas.id_cliente='" + jTextField15.getText() + "' order by facturas.id_factura asc";
                m.Listar(Aux, jTable1);
            }
            if (Tipo == "Fecha De Facturacion") {

                String FechaInicial = m.ConvertirFechaString(jDateChooser2);
                String FechaFinal = m.ConvertirFechaString(jDateChooser1);
                Aux = SentenciaSql + " && facturas.fecha_factura between '" + FechaInicial + "' and '" + FechaFinal + "' order by facturas.id_factura asc";
                m.Listar(Aux, jTable1);

            }

            if (Tipo == "Factura Mayor A") {

                Aux = SentenciaSql + " && facturas.total_factura > '" + jTextField1.getText() + "' order by facturas.id_factura asc";
                m.Listar(Aux, jTable1);
            }
            if (Tipo == "Factura Menor A") {

                Aux = SentenciaSql + " && facturas.total_factura < '" + jTextField1.getText() + "' order by facturas.id_factura asc";
                m.Listar(Aux, jTable1);
            }
            ListarDetalleFacturasInicial();
        }

    }

