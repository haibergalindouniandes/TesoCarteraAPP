    public void CrearReporte() {

        Patch = "C:\\TesoCartera\\src\\Reportes\\Report_CodigoPostal.jasper";
        try {

            Jr = (JasperReport) JRLoader.loadObjectFromFile(Patch);
            Jp = JasperFillManager.fillReport(Jr, null, Conectar);

        } catch (JRException e) {
            JOptionPane.showMessageDialog(null, "Error En Ejecucion De Jasper: " + e);
        }
    }

    public void VisualizarReporte() {

        Jv = new JasperViewer(Jp, false);
        Jv.setVisible(true);
        Jv.setTitle("PAISES REGISTRADOS");
    }

