    void ValidarBanderaBtnNuevo() {

        if (BanderaNuevo == false) {
            jButton10.setText("NUEVO");
        } else {
            jButton10.setText("GRABAR");
        }

    }

    void ComprobarBtn() {

        String Aux = jButton10.getText();

        if (Aux.equals("NUEVO")) {

            Limpiar();
            ObtenerConsecutivo();
        } else {

            if (!jTextField2.getText().equals("")) {
                Grabar();
            } else {
                JOptionPane.showMessageDialog(this, "Verifique Que El Campo Pais No Este Vacio", "Verificar", JOptionPane.ERROR_MESSAGE);
            }
        }
    }

    void Grabar() {

        mcp.setIdCodPostal(Integer.parseInt(jTextField1.getText()));
        mcp.setNombrePais(jTextField2.getText());

        SentenciaSql = "'" + mcp.getIdCodPostal() + "' , '" + mcp.getNombrePais() + "'";
        m.Grabar(TablaSql, SentenciaSql);

        Limpiar();
        Listar();

    }

    void Modificar() {
        mcp.setIdCodPostal(Integer.parseInt(jTextField1.getText()));
        mcp.setNombrePais(jTextField2.getText());

        SentenciaSql = "nombre_pais='" + mcp.getNombrePais() + "' where id_cod_postal='" + mcp.getIdCodPostal() + "'";
        m.Modificar(TablaSql, SentenciaSql);
        Limpiar();
        Listar();
    }

    void Eliminar() {
        mcp.setIdCodPostal(Integer.parseInt(jTextField1.getText()));
        SentenciaSql = "id_cod_postal = '" + mcp.getIdCodPostal() + "'";

        System.out.println("id = " + mcp.getIdCodPostal());
        m.Eliminar(TablaSql, SentenciaSql);
        Limpiar();
        Listar();
    }

    void Listar() {
        FiltroSql = jTextField8.getText();
        String Sql = "select * from " + TablaSql + " where concat(id_cod_postal,nombre_pais) like '%" + FiltroSql + "%'"
                + " order by id_cod_postal asc";
        m.Listar(Sql, jTable1);

    }

